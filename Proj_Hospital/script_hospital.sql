
CREATE TABLE PacienteE (
    paciente_id INT PRIMARY KEY,
    nomeE VARCHAR(100),
    cpfE VARCHAR(14),
    data_nascimento DATE,
    sexoE VARCHAR(1),
    telefoneE VARCHAR(15),
    enderecoE VARCHAR(200),
    convenioE VARCHAR(50)
);

CREATE TABLE MedicoE (
    medico_id INT PRIMARY KEY,
    nomeE VARCHAR(100),
    especialidadeE VARCHAR(50)
);

CREATE TABLE Unidade_InternacaoE (
    unidade_id INT PRIMARY KEY,
    nome_unidadeE VARCHAR(100)
);

CREATE TABLE AtendimentoE (
    atendimento_id INT PRIMARY KEY,
    paciente_id INT,
    dataE DATE,
    motivoE VARCHAR(200),
    FOREIGN KEY (paciente_id) REFERENCES PacienteE(paciente_id)
);

CREATE TABLE Funcionario_RecepcaoE (
    funcionario_id INT PRIMARY KEY,
    atendimento_id INT,
    nomeE VARCHAR(100),
    cargoE VARCHAR(50),
    cpfE VARCHAR(14),
    telefoneE VARCHAR(15),
    FOREIGN KEY (atendimento_id) REFERENCES AtendimentoE(atendimento_id)
);

CREATE TABLE AgendamentoE (
    agendamento_id INT PRIMARY KEY,
    funcionario_id INT,
    paciente_id INT,
    data_agendamento DATE,
    tipo_consultaE VARCHAR(50),
    statusE VARCHAR(20),
    FOREIGN KEY (funcionario_id) REFERENCES Funcionario_RecepcaoE(funcionario_id),
    FOREIGN KEY (paciente_id) REFERENCES PacienteE(paciente_id)
);

CREATE TABLE ConsultaE (
    consulta_id INT PRIMARY KEY,
    medico_id INT,
    paciente_id INT,
    diagnosticoE VARCHAR(200),
    FOREIGN KEY (medico_id) REFERENCES MedicoE(medico_id),
    FOREIGN KEY (paciente_id) REFERENCES PacienteE(paciente_id)
);

CREATE TABLE InternacionalE (
    internacao_id INT PRIMARY KEY,
    paciente_id INT,
    unidade_id INT,
    medico_id INT,
    motivo_internacaoE VARCHAR(200),
    data_admissao DATE,
    data_saida DATE,
    FOREIGN KEY (paciente_id) REFERENCES PacienteE(paciente_id),
    FOREIGN KEY (unidade_id) REFERENCES Unidade_InternacaoE(unidade_id),
    FOREIGN KEY (medico_id) REFERENCES MedicoE(medico_id)
);

CREATE TABLE VisitaE (
    visita_id INT PRIMARY KEY,
    paciente_id INT,
    data_visita DATE,
    nome_visitanteE VARCHAR(100),
    relacionamento_visitanteE VARCHAR(50),
    FOREIGN KEY (paciente_id) REFERENCES PacienteE(paciente_id)
);

